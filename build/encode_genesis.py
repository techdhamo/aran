def format_c_array(data: bytes) -> str:
    """Format bytes as C array literal."""
    lines = []
    for i in range(0, len(data), 8):
        line = ", ".join(f"0x{b:02X}" for b in data[i:i+8])
        lines.append("    " + line + ",")
    return "\n".join(lines).rstrip(",")


