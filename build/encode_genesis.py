def main():
    parser = argparse.ArgumentParser(
        description="Generate production Genesis Anchor headers"
    )
    parser.add_argument(
        "--platform",
        choices=["android", "ios"],
        required=True,
        help="Target platform"
    )
    parser.add_argument(
        "--output",
        required=True,
        help="Output header file path"
    )
    parser.add_argument(
        "--endpoint",
        default="https://api.aran.mazhai.org",
        help="Sync endpoint URL"
    )
    parser.add_argument(
        "--license-endpoint",
        default="https://api.aran.mazhai.org/api/v1/license/validate",
        help="License validation endpoint URL"
    )
    parser.add_argument(
        "--sync-interval",
        type=int,
        default=60,
        help="Sync interval in seconds"
    )
    
    args = parser.parse_args()
    
    # Load keys from environment (or generate random for testing)
    aes_key_hex = os.environ.get("ARAN_PROD_AES_KEY")
    hmac_secret_hex = os.environ.get("ARAN_PROD_HMAC_SECRET")
    pin0_sha256_b64 = os.environ.get("ARAN_PROD_TLS_PIN_0_SHA256")
    pin1_sha256_b64 = os.environ.get("ARAN_PROD_TLS_PIN_1_SHA256")
    
    # For CI/CD, keys MUST be provided. For local testing, we can generate.
    if not all([aes_key_hex, hmac_secret_hex]):
        if os.environ.get("CI") == "true":
            print("ERROR: Production keys must be set via environment variables in CI/CD", file=sys.stderr)
            sys.exit(1)
        print("WARNING: Generating random keys for local testing only", file=sys.stderr)
        aes_key = secrets.token_bytes(32)
        hmac_secret = secrets.token_bytes(32)
        salt = secrets.token_bytes(16)
        pin0_sha256 = secrets.token_bytes(32)
        pin1_sha256 = secrets.token_bytes(32)
    else:
        aes_key = bytes.fromhex(aes_key_hex)
        hmac_secret = bytes.fromhex(hmac_secret_hex)
        salt = secrets.token_bytes(16)  # Salt is always random per build
        
        if pin0_sha256_b64 and pin1_sha256_b64:
            pin0_sha256 = base64.b64decode(pin0_sha256_b64)
            pin1_sha256 = base64.b64decode(pin1_sha256_b64)
        else:
            print("WARNING: TLS pins not provided, using random values", file=sys.stderr)
            pin0_sha256 = secrets.token_bytes(32)
            pin1_sha256 = secrets.token_bytes(32)
    
    # Blind the pins
    pin0_blinded = sha256_blind(pin0_sha256, salt)
    pin1_blinded = sha256_blind(pin1_sha256, salt)
    
    # Generate header
    if args.platform == "android":
        header = generate_android_header(
            aes_key, hmac_secret, pin0_blinded, pin1_blinded,
            salt, args.endpoint, args.license_endpoint, args.sync_interval
        )
    else:
        header = generate_ios_header(
            aes_key, hmac_secret, pin0_blinded, pin1_blinded,
            salt, args.endpoint, args.license_endpoint, args.sync_interval
        )
    
    # Write output
    os.makedirs(os.path.dirname(args.output), exist_ok=True)
    with open(args.output, "w") as f:
        f.write(header)
    
    print(f"Generated production Genesis header: {args.output}")
    print(f"  Platform: {args.platform}")
    print(f"  AES-256 Key: {len(aes_key)} bytes")
    print(f"  HMAC Secret: {len(hmac_secret)} bytes")
    print(f"  Blinding Salt: {len(salt)} bytes (random per build)")
    print(f"  TLS Pin 0: {len(pin0_blinded)} bytes (blinded)")
    print(f"  TLS Pin 1: {len(pin1_blinded)} bytes (blinded)")
    print(f"  Endpoint: {args.endpoint}")
    print(f"  Sync Interval: {args.sync_interval}s")


if __name__ == "__main__":
    main()
