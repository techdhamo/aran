"""
Genesis Anchor — Production Build Encoder

Generates pre-computed XOR-chain encoded const blobs for production releases.
This script runs in CI/CD and produces C++ header files that are compiled
into the SDK, eliminating runtime key generation in production builds.

Usage:
    export ARAN_PROD_AES_KEY=$(openssl rand -hex 32)
    export ARAN_PROD_HMAC_SECRET=$(openssl rand -hex 32)
    export ARAN_PROD_TLS_PIN_0_SHA256="sha256_of_cert_0"
    export ARAN_PROD_TLS_PIN_1_SHA256="sha256_of_cert_1"
    python3 build/encode_genesis.py --platform android --output aran-android-sdk/aran-secure/src/main/cpp/genesis_prod.h
    python3 build/encode_genesis.py --platform ios --output aran-ios-sdk/Aran/Aran/Sources/AranGenesis_prod.h

Security:
    - NEVER commit production keys to git
    - Use GitHub Actions secrets or vault (HashiCorp, AWS KMS)
    - Generated header files should be .gitignored
    - CI/CD should generate fresh keys per release
"""

import argparse
import base64
import hashlib
import os
import secrets
import sys
from typing import List, Tuple


# ============================================================================
# XOR-Chain Obfuscation (must match C++ implementation)
# ============================================================================

GENESIS_SEED = 0xA7
GENESIS_FK_AES = 0x5C
GENESIS_FK_HMAC = 0x3E
GENESIS_FK_PIN0 = 0x71
GENESIS_FK_PIN1 = 0x8D
GENESIS_FK_SALT = 0xC3
GENESIS_FK_ENDPOINT = 0x2B
GENESIS_FK_LICENSE = 0x94


def rol8(val: int, n: int) -> int:
    """Rotate left 8-bit value by n bits."""
    return ((val << n) | (val >> (8 - n))) & 0xFF


def xor_chain_encode(plain: bytes, field_key: int) -> bytes:
    """
    XOR-chain + bitwise rotation obfuscation.
    Same algorithm as aran_genesis.cpp:
        encoded[0] = plain[0] ^ (seed ^ field_key)
        encoded[i] = plain[i] ^ ROL8(encoded[i-1], 3) ^ (i*0x37) ^ field_key
    """
    if len(plain) == 0:
        return b""
    
    encoded = bytearray(len(plain))
    encoded[0] = plain[0] ^ (GENESIS_SEED ^ field_key)
    
    for i in range(1, len(plain)):
        rotated = rol8(encoded[i - 1], 3)
        encoded[i] = plain[i] ^ rotated ^ ((i * 0x37) & 0xFF) ^ field_key
    
    return bytes(encoded)


def sha256_blind(pin_sha256: bytes, salt: bytes) -> bytes:
    """Blind a pin hash: SHA256(salt || pin_hash)."""
    h = hashlib.sha256()
    h.update(salt)
    h.update(pin_sha256)
    return h.digest()


# ============================================================================
# Header Generation
# ============================================================================

def generate_android_header(
    aes_key: bytes,
    hmac_secret: bytes,
    pin0_blinded: bytes,
    pin1_blinded: bytes,
    salt: bytes,
    endpoint: str,
    license_endpoint: str,
    sync_interval: int = 60
) -> str:
    """Generate C++ header for Android SDK."""
    
    # XOR-chain encode each field
    enc_aes = xor_chain_encode(aes_key, GENESIS_FK_AES)
    enc_hmac = xor_chain_encode(hmac_secret, GENESIS_FK_HMAC)
    enc_pin0 = xor_chain_encode(pin0_blinded, GENESIS_FK_PIN0)
    enc_pin1 = xor_chain_encode(pin1_blinded, GENESIS_FK_PIN1)
    enc_salt = xor_chain_encode(salt, GENESIS_FK_SALT)
    enc_endpoint = xor_chain_encode(endpoint.encode(), GENESIS_FK_ENDPOINT)
    enc_license = xor_chain_encode(license_endpoint.encode(), GENESIS_FK_LICENSE)
    
    header = f'''// Copyright 2024-2026 Mazhai Technologies
// Licensed under the Apache License, Version 2.0
//
// WARNING: AUTO-GENERATED FILE — DO NOT EDIT
// Generated by build/encode_genesis.py at {os.environ.get("GITHUB_RUN_ID", "local")}
//
// This file contains PRODUCTION cryptographic material.
// It should be generated fresh per release in CI/CD and never committed.

#ifndef ARAN_GENESIS_PROD_H
#define ARAN_GENESIS_PROD_H

#include <cstdint>
#include <cstddef>

// ============================================================================
// Production Genesis State — Pre-computed XOR-chain Encoded Blobs
// ============================================================================

// AES-256 Key (32 bytes)
static const uint8_t _GENESIS_PROD_AES_KEY[{len(enc_aes)}] = {{
    {format_c_array(enc_aes)}
}};

// HMAC-SHA256 Secret (32 bytes)
static const uint8_t _GENESIS_PROD_HMAC_SECRET[{len(enc_hmac)}] = {{
    {format_c_array(enc_hmac)}
}};

// Blinded TLS Pin 0 (32 bytes)
static const uint8_t _GENESIS_PROD_PIN0[{len(enc_pin0)}] = {{
    {format_c_array(enc_pin0)}
}};

// Blinded TLS Pin 1 (32 bytes)
static const uint8_t _GENESIS_PROD_PIN1[{len(enc_pin1)}] = {{
    {format_c_array(enc_pin1)}
}};

// Blinding Salt (16 bytes)
static const uint8_t _GENESIS_PROD_SALT[{len(enc_salt)}] = {{
    {format_c_array(enc_salt)}
}};

// Sync Endpoint (null-terminated)
static const uint8_t _GENESIS_PROD_ENDPOINT[{len(enc_endpoint)}] = {{
    {format_c_array(enc_endpoint)}
}};

// License Endpoint (null-terminated)
static const uint8_t _GENESIS_PROD_LICENSE_EP[{len(enc_license)}] = {{
    {format_c_array(enc_license)}
}};

// Sync interval in seconds
static const uint32_t _GENESIS_PROD_SYNC_INTERVAL = {sync_interval};

// Default reaction policy (0=ignore, 1=alert, 2=block, 3=kill)
static const uint32_t _GENESIS_PROD_REACTION_POLICY = 3;  // KILL_APP

// ============================================================================
// Production State Loader (replaces runtime encoding)
// ============================================================================

static inline void aran_load_production_genesis(AranGenesisState* state) {{
    // Decode AES key
    aran_xor_chain_decode(_GENESIS_PROD_AES_KEY, state->aes_key, 
                          sizeof(_GENESIS_PROD_AES_KEY), {GENESIS_FK_AES});
    
    // Decode HMAC secret
    aran_xor_chain_decode(_GENESIS_PROD_HMAC_SECRET, state->hmac_secret,
                          sizeof(_GENESIS_PROD_HMAC_SECRET), {GENESIS_FK_HMAC});
    
    // Decode pins
    aran_xor_chain_decode(_GENESIS_PROD_PIN0, state->tls_pin0,
                          sizeof(_GENESIS_PROD_PIN0), {GENESIS_FK_PIN0});
    aran_xor_chain_decode(_GENESIS_PROD_PIN1, state->tls_pin1,
                          sizeof(_GENESIS_PROD_PIN1), {GENESIS_FK_PIN1});
    
    // Decode salt
    aran_xor_chain_decode(_GENESIS_PROD_SALT, state->blinding_salt,
                          sizeof(_GENESIS_PROD_SALT), {GENESIS_FK_SALT});
    
    // Decode endpoints
    aran_xor_chain_decode(_GENESIS_PROD_ENDPOINT, (volatile uint8_t*)state->sync_endpoint,
                          sizeof(_GENESIS_PROD_ENDPOINT), {GENESIS_FK_ENDPOINT});
    aran_xor_chain_decode(_GENESIS_PROD_LICENSE_EP, (volatile uint8_t*)state->license_endpoint,
                          sizeof(_GENESIS_PROD_LICENSE_EP), {GENESIS_FK_LICENSE});
    
    state->sync_interval_seconds = _GENESIS_PROD_SYNC_INTERVAL;
    state->default_reaction_policy = _GENESIS_PROD_REACTION_POLICY;
}}

#endif // ARAN_GENESIS_PROD_H
'''
    return header


